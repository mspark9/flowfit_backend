def _existing_file_names() -> set[str]:
    """이미 등록된 (활성) 시드 파일명 집합을 반환 — 중복 업로드 방지용."""
    conn = get_connection()
    cur = conn.cursor()
    try:
        cur.execute(
            """
            SELECT file_name FROM sales_proposal_documents
            WHERE deleted_at IS NULL AND is_active = TRUE
            """
        )
        rows = cur.fetchall()
    finally:
        cur.close()
        conn.close()
    return {row[0] for row in rows}


def main() -> None:
    existing = _existing_file_names()
    uploaded = 0
    skipped = 0

    for case in SEED_CASES:
        file_name = case["file_name"]
        if file_name in existing:
            print(f"[seed] SKIP  {file_name} (이미 존재)")
            skipped += 1
            continue

        content_bytes = case["content"].encode("utf-8")
        print(f"[seed] 업로드 {case['industry']:<10} {file_name}")
        save_proposal_document(
            filename=file_name,
            file_bytes=content_bytes,
            industry=case["industry"],
            uploader={
                "employee_id": "system",
                "name": "시드 스크립트",
                "department": "영업/영업관리팀",
            },
        )
        uploaded += 1

    print(f"\n[seed] 완료 — 신규 {uploaded}건, 건너뜀 {skipped}건")


if __name__ == "__main__":
    main()
